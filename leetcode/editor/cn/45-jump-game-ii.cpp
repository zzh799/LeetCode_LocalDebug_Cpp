class Solution {
public:
    int jump(vector<int>& nums) {
        int next_farest=0;
        int step=0;
        int cur_farest=0;
        for(int i =0;i<nums.size()-1;i++){
            next_farest = max(next_farest,i+nums[i]);
            if(cur_farest==i) {
                cur_farest = next_farest;
                step++;
            }
        }
        return step;
    }
};

