class Solution {
public:
    char getPair(char c){
        char res=' ';
        switch (c){
            case '(':res =')' ;
                break;
            case '{':res ='}' ;
                break;
            case '[':res =']' ;
                break;
        }
        return res;
    }

    bool isValid(string s) {
        vector<char> stack;

        for(char c:s){
            if(stack.size()==0) {
                stack.push_back(c);
                continue;
            }
            if(c==getPair(stack.back())) {
                stack.pop_back();
            }
            else{
                stack.push_back(c);
            }
        }
        return stack.size()==0;

    }
};
