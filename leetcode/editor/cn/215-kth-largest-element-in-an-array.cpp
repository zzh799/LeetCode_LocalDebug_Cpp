class Solution {
public:
    int findKthLargest(vector<int>& nums, int k) {
        int result= 0;
        int n = int(nums.size());
        if(n==0||k>n) return result;

        priority_queue<int,vector<int>,greater<int>> queue;

        for(int num:nums){
            queue.push(num);
            if(queue.size()>k){
                queue.pop();
            }
        }
        result = queue.top();
        return result;
    }
};
