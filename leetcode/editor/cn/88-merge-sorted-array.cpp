class Solution {
public:
    void merge(vector<int>& nums1, int m, vector<int>& nums2, int n) {
        if(m==0) nums1=nums2;

        int i=m-1;
        int j=n-1;
        int pos=nums1.size()-1;

        while(i>-1&&j>-1){
            if(nums1[i]<nums2[j]){
                nums1[pos--]=nums2[j--];

            }else if(nums1[i]>nums2[j]){
                nums1[pos--]=nums1[i--];
            }else{
                nums1[pos--]=nums1[i--];
                nums1[pos--]=nums2[j--];
            }
        }

        while(j>-1){
            nums1[pos--]=nums2[j--];
        }
    }
};
