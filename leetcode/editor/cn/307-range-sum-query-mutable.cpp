class NumArray {
    vector<int> sums;
    vector<int> data;

public:
    NumArray(vector<int> &nums) {
        data = nums;
        sums = vector<int>(data.size() + 1, 0);
        for (int i = 0; i < data.size(); i++) {
            add(i + 1, data[i]);
        }
    }

    void add(int i, int val) {
        while (i <= data.size()) {
            sums[i] += val;
            i += i & -i;
        }
    }

    int query(int i) {
        int ans = 0;
        while (i > 0) {
            ans += sums[i];
            i -= i & -i;
        }
        return ans;
    }

    void update(int i, int val) {
        data[i] = val;
        add(i + 1, val - data[i]);
    }

    int sumRange(int i, int j) {
        return query(j + 1) - query(i);
    }
};

